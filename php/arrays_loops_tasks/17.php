<?php
echo "<b>Месяцы:</b>";
$month = date("m");
$months = array('01' => 'Январь', '02' => 'Февраль', '03' => 'Март', '04' => 'Апрель', '05' => 'Май', '06' => 'Июнь',
				'07' => 'Июль', '08' => 'Август', '09' => 'Сентябрь', '10' => 'Октябрь', '11' => 'Ноябрь', '12' => 'Декабрь');
foreach ($months as $key => $value) {
	if ($month == $key) {
		echo "<br><b>{$value}</b>";
	} else {
		echo "<br>{$value}";
	}
}
?>